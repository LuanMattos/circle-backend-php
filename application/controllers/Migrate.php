    private function _function()
    {
        $this->db->query("CREATE OR REPLACE FUNCTION random_between(low INT ,high INT)
                            RETURNS text AS
                        $$
                            BEGIN
                                RETURN floor(random()* (high-low + 1) + low);
                            END;
        $$ language 'plpgsql' STRICT;");
    }

